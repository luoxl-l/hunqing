    // 1.加入登录了，就在当前页面
    // 2.如果没有登录跳转到登录页面
    // 通过本地存储进行判断
    var isLogin = wx.getStorageSync("islogin")
    if (!isLogin) {
      wx.navigateTo({
        url: '/pages/login/login',
      })
    } else {
      var userInfo = wx.getStorageSync("userInfo")
      that.setData({
        userInfo: userInfo
      })
    }
